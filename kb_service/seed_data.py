def expand_to_500():
    """基于已有条目生成变体，凑够 500+ 条"""
    import random
    random.seed(42)

    current_count = len(SEED_DATA)
    needed = max(0, 500 - current_count)
    print(f"[expand] 基础数据 {current_count} 条，需要扩充 {needed} 条")

    if needed <= 0:
        return SEED_DATA

    # 从已有条目中随机选，改写措辞生成变体
    expanded = []
    templates = [
        "请问{}？",
        "麻烦问一下，{}",
        "求助：{}要怎么处理？",
        "{}应该怎么办？在线等。",
        "遇到一个问题：{}",
    ]

    answer_templates = [
        "关于您提到的这个问题，{}",
        "处理步骤如下：{}",
        "建议这样操作：{}",
        "可以参考这个方案：{}",
    ]

    for i in range(needed):
        # 随机选一条基础数据
        src = SEED_DATA[i % len(SEED_DATA)]
        q = src["question"]
        a = src["answer"]
        cat = src["metadata"]["category"]

        # 10% 概率改写问题措辞
        if random.random() < 0.1:
            q = random.choice(templates).format(q.replace("？", "").replace("怎么", "如何").replace("如何", "怎样"))
            # 确保以问号结尾
            if not q.endswith("？") and not q.endswith("?"):
                q = q + "？"

        expanded.append({
            "question": q,
            "answer": a,
            "metadata": {"source": "expanded", "category": cat, "batch": "auto_expand"}
        })

    print(f"[expand] 生成变体 {len(expanded)} 条")
    return expanded


